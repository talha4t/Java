public class Main {
    public static void main(String[] args) {

        // implicit casting
        double price = 100.00;
        double finalPrice = price + 19;

        System.out.println(finalPrice);

        // explicit casting
        int p = 100;
        int fP = p + (int)19.00;

        System.out.println(fP);

        // Constants

        final float PI = 3.1416f;

        System.out.println(PI);

    }
}